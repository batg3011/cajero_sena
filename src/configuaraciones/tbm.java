    public void MostrarDatos(){
    
   DefaultTableModel modelo=new DefaultTableModel(); //instancia de tabla
    
        modelo.addColumn("TU CUENTA");
        modelo.addColumn("COD DE RESIBO");
        modelo.addColumn("NOMBRE");
        modelo.addColumn("TIPO DE TRANSACCION");
        modelo.addColumn("VALOR ");
        tb.setModel(modelo);
        
        String [] datos = new String[5];
        
           try{

           Statement st = cn.createStatement();
           ResultSet rs = st.executeQuery("SELECT * FROM movimientos_cu WHERE id_cu='" + ctin.getText() + "'");
          
           while (rs.next()){
           
               datos[0]=rs.getString(5);
               datos[1]=rs.getString(1);
               datos[2]=rs.getString(2);
               datos[3]=rs.getString(3);
               datos[4]=rs.getString(4);
               
               modelo.addRow(datos);
         
       
           }
 
        }catch(Exception e){
            System.out.print(e.getMessage());
            

        }
        
        
    } 
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
